#include<iostream>
using namespace std;

int main(){
    int a=6, b=5;
    cout<<"Operators in C++:"<<endl;
    cout<<"following are the types of arithmetic operators:"<<endl;
    cout<<"The value of a+b is :"<<a+b<<endl;
    cout<<"The value of a_b is :"<<a-b<<endl;
    cout<<"The value of a*b is :"<<a*b<<endl;
    cout<<"The value of a/b is :"<<a/b<<endl;
    cout<<"The value of a%b is :"<<a%b<<endl;
    cout<<"The value of a++ is :"<<a++<<endl;
    cout<<"The value of a-- is :"<<a--<<endl;
    cout<<"The value of ++a is :"<<++a<<endl;
    cout<<"The value of --a is :"<<--a<<endl;
    return 0;
}/*Comparsion operator always gives boolean value.
logical operator "and" and "or" operator also gives boolean value.*/